const SidebarButton = ({ item, isActive, isExpanded, onClick }) => {
  const [hovered, setHovered] = useState(false);

  const foregroundColor =
    isActive || hovered ? colours.secondary : colours.mutedText;

  return (
    <button
      type="button"
      onClick={onClick}
      onMouseEnter={() => setHovered(true)}
      onMouseLeave={() => setHovered(false)}
      className="w-full px-3 py-3 rounded-lg text-sm font-medium flex items-center justify-between transition-all duration-200 cursor-pointer"
      style={{
        backgroundColor: isActive
          ? colours.surface
          : hovered
            ? colours.primary
            : "transparent",
        color: foregroundColor,
        border: "none",
        fontFamily: fonts.secondary,
      }}
    >
      <div className="flex items-center gap-3">
        <span
          className="flex shrink-0"
          style={{ color: foregroundColor }}
        >
          {item.icon("w-5 h-5 transition-colors duration-200")}
        </span>

        <span>{item.label}</span>
      </div>

      <svg
        className={`w-4 h-4 transition-transform duration-300 ${
          isExpanded ? "rotate-180" : ""
        }`}
        style={{ color: foregroundColor }}
        fill="none"
        stroke="currentColor"
        strokeWidth="2.5"
        viewBox="0 0 24 24"
        xmlns="http://www.w3.org/2000/svg"
      >
        <path
          strokeLinecap="round"
          strokeLinejoin="round"
          d="M19.5 8.25l-7.5 7.5-7.5-7.5"
        />
      </svg>
    </button>
  );
};

const SidebarNavLink = ({ to, label, icon, end }) => {
  const [hovered, setHovered] = useState(false);
  const location = useLocation();

  const customIsActive = end
    ? location.pathname === to
    : (location.pathname === to || location.pathname.startsWith(to + "/"));

  return (
    <NavLink
      to={to}
      end={end}
      onMouseEnter={() => setHovered(true)}
      onMouseLeave={() => setHovered(false)}
      className="px-3 py-3 rounded-lg text-sm font-medium flex items-center gap-3 transition-all duration-200 no-underline w-full"
      style={() => ({
        backgroundColor: customIsActive
          ? colours.accent
          : hovered
            ? colours.surface
            : "transparent",
        color: customIsActive
          ? colours.background
          : hovered
            ? colours.secondary
            : colours.mutedText,
        fontFamily: fonts.secondary,
      })}
    >
      {() => {
        const foregroundColor = customIsActive
          ? colours.background
          : hovered
            ? colours.secondary
            : colours.mutedText;

        return (
          <>
            <span
              className="flex shrink-0"
              style={{ color: foregroundColor }}
            >
              {icon("w-5 h-5 transition-colors duration-200")}
            </span>

            <span>{label}</span>
          </>
        );
      }}
    </NavLink>
  );
};

