const AdminSidebar = () => {
  const [isOpen, setIsOpen] = useState(false);
  const [menuButtonHovered, setMenuButtonHovered] = useState(false);
  const [expandedSections, setExpandedSections] = useState({});

  const location = useLocation();

  const sidebarStyle = {
    backgroundColor: colours.subBackground,
    color: colours.text,
    borderColor: colours.border,
    fontFamily: fonts.secondary,
  };

  const titleStyle = {
    color: colours.secondary,
    fontFamily: fonts.primary,
  };

  useEffect(() => {
    setIsOpen(false);
  }, [location.pathname]);

  useEffect(() => {
    document.body.style.overflow = isOpen ? "hidden" : "";

    return () => {
      document.body.style.overflow = "";
    };
  }, [isOpen]);

  useEffect(() => {
    const newExpanded = { ...expandedSections };
    let hasUpdates = false;

    menuTree.forEach((item) => {
      if (!item.children) return;

      const isChildActive = item.children.some(
        (child) =>
          location.pathname === child.path ||
          location.pathname.startsWith(child.path + "/"),
      );

      if (isChildActive && !expandedSections[item.label]) {
        newExpanded[item.label] = true;
        hasUpdates = true;
      }
    });

    if (hasUpdates) {
      setExpandedSections(newExpanded);
    }
  }, [location.pathname]);

  const toggleSection = (label) => {
    setExpandedSections((previous) => ({
      ...previous,
      [label]: !previous[label],
    }));
  };

  const renderMenuItem = (item) => {
    const hasChildren = Boolean(item.children?.length);
    const isExpanded = Boolean(expandedSections[item.label]);

    if (hasChildren) {
      const isAnyChildActive = item.children.some(
        (child) =>
          location.pathname === child.path ||
          location.pathname.startsWith(child.path + "/"),
      );

      return (
        <div
          key={item.label}
          className="flex flex-col gap-1 w-full"
        >
          <SidebarButton
            item={item}
            isActive={isAnyChildActive}
            isExpanded={isExpanded}
            onClick={() => toggleSection(item.label)}
          />

          <div
            className={`grid transition-all duration-300 ease-in-out ${
              isExpanded
                ? "grid-rows-[1fr] opacity-100 mt-1"
                : "grid-rows-[0fr] opacity-0 pointer-events-none"
            }`}
          >
            <div className="overflow-hidden">
              <div
                className="flex flex-col gap-1 pl-4 border-l ml-6 py-1"
                style={{ borderColor: colours.border }}
              >
                {item.children.map((child) => (
                  <SidebarNavLink
                    key={child.path}
                    to={child.path}
                    label={child.label}
                    icon={child.icon}
                  />
                ))}
              </div>
            </div>
          </div>
        </div>
      );
    }

    return (
      <SidebarNavLink
        key={item.path}
        to={item.path}
        end={item.path === "/admin"}
        label={item.label}
        icon={item.icon}
      />
    );
  };

  return (
    <div
      style={{
        backgroundColor: colours.subBackground,
        color: colours.text,
      }}
    >
      {/* Desktop sidebar */}
      <aside
        data-lenis-prevent
        className="hidden md:flex w-64 border-r p-6 h-screen sticky top-0 shrink-0 flex-col justify-between"
        style={sidebarStyle}
      >
        <div className="overflow-y-auto flex-1 pr-1 no-scrollbar">
          <h2
            className="text-xl font-bold tracking-wide mb-8 px-4"
            style={titleStyle}
          >
            ETLAWM Admin
          </h2>

          <nav className="flex flex-col gap-1.5">
            {menuTree.map((item) => renderMenuItem(item))}
          </nav>
        </div>

        <div
          className="pt-4 border-t mt-4 shrink-0"
          style={{ borderColor: colours.border }}
        >
          <WebsiteLink />
        </div>
      </aside>

      {/* Mobile header */}
      <header
        className="md:hidden sticky top-0 z-[100] w-full backdrop-blur-md border-b px-4 py-3.5 flex items-center justify-between"
        style={{
          backgroundColor: colours.subBackground,
          borderColor: colours.border,
        }}
      >
        <span
          className="text-lg font-bold tracking-wide"
          style={titleStyle}
        >
          ETLAWM Admin
        </span>

        <button
          type="button"
          onClick={() => setIsOpen((previous) => !previous)}
          onMouseEnter={() => setMenuButtonHovered(true)}
          onMouseLeave={() => setMenuButtonHovered(false)}
          aria-label="Toggle admin menu"
          className="flex flex-col items-center justify-center w-8 h-8 rounded-lg transition-colors relative z-[110] cursor-pointer border-none"
          style={{
            backgroundColor: menuButtonHovered
              ? colours.surface
              : "transparent",
          }}
        >
          <div className="flex flex-col gap-1.5 w-5">
            <span
              className="block h-[1.5px] w-full transition-all duration-300"
              style={{
                backgroundColor: colours.secondary,
                transform: isOpen
                  ? "rotate(45deg) translate(5px, 5px)"
                  : "none",
              }}
            />

            <span
              className="block h-[1.5px] w-full transition-all duration-300"
              style={{
                backgroundColor: colours.secondary,
                opacity: isOpen ? 0 : 1,
              }}
            />

            <span
              className="block h-[1.5px] w-full transition-all duration-300"
              style={{
                backgroundColor: colours.secondary,
                transform: isOpen
                  ? "rotate(-45deg) translate(5px, -5px)"
                  : "none",
              }}
            />
          </div>
        </button>
      </header>

      {/* Mobile overlay */}
      <div
        onClick={() => setIsOpen(false)}
        className={`fixed inset-0 z-[105] backdrop-blur-xs transition-opacity duration-300 md:hidden ${
          isOpen
            ? "opacity-100 pointer-events-auto"
            : "opacity-0 pointer-events-none"
        }`}
        style={{
          backgroundColor: `color-mix(
            in srgb,
            ${colours.text} 30%,
            transparent
          )`,
        }}
      />

      {/* Mobile sidebar */}
      <div
        data-lenis-prevent
        className={`fixed inset-y-0 left-0 z-[108] w-72 max-w-[80vw] border-r shadow-2xl flex flex-col p-6 transition-transform duration-300 ease-out md:hidden ${
          isOpen ? "translate-x-0" : "-translate-x-full"
        }`}
        style={sidebarStyle}
      >
        <div className="flex items-center justify-between mb-8">
          <span
            className="text-xl font-bold tracking-wide"
            style={titleStyle}
          >
            ETLAWM Admin
          </span>
        </div>

        <nav className="flex flex-col gap-1.5 overflow-y-auto flex-1 pr-1 no-scrollbar">
          {menuTree.map((item) => renderMenuItem(item))}
        </nav>

        <div
          className="pt-4 border-t mt-4 shrink-0"
          style={{ borderColor: colours.border }}
        >
          <WebsiteLink />
        </div>
      </div>
    </div>
  );
};

export default AdminSidebar;
